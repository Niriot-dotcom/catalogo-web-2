export function getCurrentPageInfo(url) {
  let catalogPages;
  let catalogName = "";

  // Determinar el catálogo basado en la URL
  if (url.includes("/invierno/")) {
    catalogPages = catalogSections.INVIERNO;
    catalogName = "invierno";
  } else if (url.includes("/biasi/")) {
    catalogPages = catalogSections.BIASI;
    catalogName = "biasi";
  } else if (url.includes("/chavos/")) {
    catalogPages = catalogSections.CHAVOS;
    catalogName = "chavos";
  } else if (url.includes("/vianney/")) {
    catalogPages = catalogSections.VIANNEY;
    catalogName = "vianney";
  } else if (url.includes("/bebe/")) {
    catalogPages = catalogSections.BEBE;
    catalogName = "bebe";
  } else {
    // En caso de que la URL no coincida con ningún catálogo
    return "Páginas";
  }

  // Extraer el nombre de la página de la URL
  const pageName = url.split("/").pop();

  // Encontrar la página y su posición
  const currentPage = catalogPages.find((page) => page.link === pageName);
  if (!currentPage) {
    return "Página no encontrada en el catálogo " + catalogName;
  }

  // Devolver la posición de la página y el largo total del catálogo

  return `${currentPage.position}/${
    catalogPages.length
  } - ${catalogName} - ${currentPage.link.replace(/-/g, " ")}`;
}

